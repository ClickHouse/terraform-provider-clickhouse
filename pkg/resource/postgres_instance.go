// Create creates the resource and sets the initial Terraform state.
func (r *PostgresInstanceResource) Create(ctx context.Context, req resource.CreateRequest, resp *resource.CreateResponse) {
	// Retrieve values from plan
	var plan models.PostgresInstanceResourceModel
	diags := req.Plan.Get(ctx, &plan)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Build API request body from plan
	createReq := api.PostgresInstanceCreate{
		Name:        plan.Name.ValueString(),
		Provider:    plan.CloudProvider.ValueString(),
		Region:      plan.Region.ValueString(),
		Size:        plan.Size.ValueString(),
		StorageSize: int(plan.StorageSize.ValueInt64()),
	}

	if !plan.PostgresVersion.IsUnknown() && !plan.PostgresVersion.IsNull() {
		createReq.PostgresVersion = plan.PostgresVersion.ValueString()
	}

	if !plan.HAType.IsUnknown() && !plan.HAType.IsNull() {
		createReq.HAType = plan.HAType.ValueString()
	}

	if !plan.PgConfig.IsUnknown() && !plan.PgConfig.IsNull() {
		pgConfigMap := make(map[string]string)
		if diag := plan.PgConfig.ElementsAs(ctx, &pgConfigMap, false); diag.HasError() {
			resp.Diagnostics.Append(diag...)
			return
		}
		createReq.PgConfig = pgConfigMap
	}

	if !plan.PgBouncerConfig.IsUnknown() && !plan.PgBouncerConfig.IsNull() {
		pgBouncerConfigMap := make(map[string]string)
		if diag := plan.PgBouncerConfig.ElementsAs(ctx, &pgBouncerConfigMap, false); diag.HasError() {
			resp.Diagnostics.Append(diag...)
			return
		}
		createReq.PgBouncerConfig = pgBouncerConfigMap
	}

	if !plan.Tags.IsUnknown() && !plan.Tags.IsNull() {
		tagsMap := make(map[string]string)
		if diag := plan.Tags.ElementsAs(ctx, &tagsMap, false); diag.HasError() {
			resp.Diagnostics.Append(diag...)
			return
		}

		tags := make([]api.Tag, 0, len(tagsMap))
		for key, value := range tagsMap {
			tags = append(tags, api.Tag{
				Key:   key,
				Value: value,
			})
		}
		createReq.Tags = tags
	}

	// Create the Postgres instance
	instance, err := r.client.CreatePostgresInstance(ctx, createReq)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Creating Postgres Instance",
			"Could not create Postgres instance, unexpected error: "+err.Error(),
		)
		return
	}

	// Wait for the instance to reach "running" state
	err = r.client.WaitForPostgresInstanceState(ctx, instance.ID, func(state string) bool { return state == "running" }, 300)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Waiting for Postgres Instance",
			"Could not wait for Postgres instance to reach running state, unexpected error: "+err.Error(),
		)
		return
	}

	// Populate computed fields from the API response
	plan.ID = types.StringValue(instance.ID)
	err = r.syncPostgresState(ctx, &plan)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Reading Postgres Instance",
			"Could not read Postgres instance id "+plan.ID.ValueString()+": "+err.Error(),
		)
		return
	}

	// Set state to fully populated data
	diags = resp.State.Set(ctx, plan)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}
}

// Read refreshes the Terraform state with the latest data.
func (r *PostgresInstanceResource) Read(ctx context.Context, req resource.ReadRequest, resp *resource.ReadResponse) {
	var state models.PostgresInstanceResourceModel
	diags := req.State.Get(ctx, &state)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	instance, err := r.client.GetPostgresInstance(ctx, state.ID.ValueString())
	if api.IsNotFound(err) {
		// Resource was deleted outside Terraform
		resp.State.RemoveResource(ctx)
		return
	} else if err != nil {
		resp.Diagnostics.AddError(
			"Error Reading Postgres Instance",
			"Could not read Postgres instance id "+state.ID.ValueString()+": "+err.Error(),
		)
		return
	}

	r.syncPostgresStateFromInstance(instance, &state)

	// Set refreshed state
	diags = resp.State.Set(ctx, state)
	resp.Diagnostics.Append(diags...)
}

// Update updates the resource and sets the updated Terraform state on success.
func (r *PostgresInstanceResource) Update(ctx context.Context, req resource.UpdateRequest, resp *resource.UpdateResponse) {
	// Retrieve values from plan and state
	var plan, state models.PostgresInstanceResourceModel
	diags := req.Plan.Get(ctx, &plan)
	resp.Diagnostics.Append(diags...)
	diags = req.State.Get(ctx, &state)
	resp.Diagnostics.Append(diags...)

	if resp.Diagnostics.HasError() {
		return
	}

	postgresId := state.ID.ValueString()
	update := api.PostgresInstanceUpdate{}
	hasChange := false

	if plan.Size != state.Size {
		update.Size = plan.Size.ValueString()
		hasChange = true
	}

	if plan.StorageSize != state.StorageSize {
		storageSize := int(plan.StorageSize.ValueInt64())
		update.StorageSize = &storageSize
		hasChange = true
	}

	if plan.HAType != state.HAType {
		update.HAType = plan.HAType.ValueString()
		hasChange = true
	}

	if !plan.Tags.Equal(state.Tags) {
		hasChange = true

		// Tags use FULL REPLACEMENT — send complete []Tag array
		tags := make([]api.Tag, 0)
		if !plan.Tags.IsNull() {
			tagsMap := make(map[string]string)
			if diag := plan.Tags.ElementsAs(ctx, &tagsMap, false); diag.HasError() {
				resp.Diagnostics.Append(diag...)
				return
			}

			for key, value := range tagsMap {
				tags = append(tags, api.Tag{
					Key:   key,
					Value: value,
				})
			}
		}
		update.Tags = tags
	}

	if hasChange {
		_, err := r.client.UpdatePostgresInstance(ctx, postgresId, update)
		if err != nil {
			resp.Diagnostics.AddError(
				"Error Updating Postgres Instance",
				"Could not update Postgres instance, unexpected error: "+err.Error(),
			)
			return
		}

		// Wait for the instance to reach "running" state after update
		err = r.client.WaitForPostgresInstanceState(ctx, postgresId, func(state string) bool { return state == "running" }, 300)
		if err != nil {
			resp.Diagnostics.AddError(
				"Error Waiting for Postgres Instance",
				"Could not wait for Postgres instance to reach running state after update, unexpected error: "+err.Error(),
			)
			return
		}
	}

	// Sync the latest state from the API
	plan.ID = state.ID
	err := r.syncPostgresState(ctx, &plan)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Reading Postgres Instance",
			"Could not read Postgres instance id "+plan.ID.ValueString()+": "+err.Error(),
		)
		return
	}

	// Set updated state
	diags = resp.State.Set(ctx, plan)
	resp.Diagnostics.Append(diags...)
}

// Delete deletes the resource and removes the Terraform state on success.
func (r *PostgresInstanceResource) Delete(ctx context.Context, req resource.DeleteRequest, resp *resource.DeleteResponse) {
	// Retrieve values from state
	var state models.PostgresInstanceResourceModel
	diags := req.State.Get(ctx, &state)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	err := r.client.DeletePostgresInstance(ctx, state.ID.ValueString())
	if api.IsNotFound(err) {
		// Already deleted
		return
	}
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Deleting Postgres Instance",
			"Could not delete Postgres instance, unexpected error: "+err.Error(),
		)
		return
	}
}

