// ModifyPlan validates plan modifications before apply.
func (r *PostgresInstanceResource) ModifyPlan(ctx context.Context, req resource.ModifyPlanRequest, resp *resource.ModifyPlanResponse) {
	if req.Plan.Raw.IsNull() {
		// If the entire plan is null, the resource is planned for destruction.
		return
	}

	if req.State.Raw.IsNull() {
		// Creating a new resource, no validations needed.
		return
	}

	var plan, state models.PostgresInstanceResourceModel
	diags := req.Plan.Get(ctx, &plan)
	resp.Diagnostics.Append(diags...)
	diags = req.State.Get(ctx, &state)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Check that storage_size is not decreasing
	if !plan.StorageSize.IsUnknown() && !state.StorageSize.IsNull() {
		if plan.StorageSize.ValueInt64() < state.StorageSize.ValueInt64() {
			resp.Diagnostics.AddAttributeError(
				path.Root("storage_size"),
				"Invalid Update",
				"Postgres storage can only be increased, not decreased",
			)
		}
	}
}

// syncPostgresState fetches the latest Postgres instance from the API and updates the model.
func (r *PostgresInstanceResource) syncPostgresState(ctx context.Context, state *models.PostgresInstanceResourceModel) error {
	instance, err := r.client.GetPostgresInstance(ctx, state.ID.ValueString())
	if err != nil {
		return fmt.Errorf("error fetching Postgres instance: %w", err)
	}

	r.syncPostgresStateFromInstance(instance, state)
	return nil
}

// syncPostgresStateFromInstance maps an api.PostgresInstance to the Terraform state model.
func (r *PostgresInstanceResource) syncPostgresStateFromInstance(instance *api.PostgresInstance, state *models.PostgresInstanceResourceModel) {
	state.ID = types.StringValue(instance.ID)
	state.Name = types.StringValue(instance.Name)
	state.CloudProvider = types.StringValue(instance.Provider)
	state.Region = types.StringValue(instance.Region)
	state.PostgresVersion = types.StringValue(instance.PostgresVersion)
	state.Size = types.StringValue(instance.Size)
	state.StorageSize = types.Int64Value(int64(instance.StorageSize))
	state.HAType = types.StringValue(instance.HAType)
	state.State = types.StringValue(instance.State)
	state.IsPrimary = types.BoolValue(instance.IsPrimary)
	state.Hostname = types.StringValue(instance.Hostname)
	state.ConnectionString = types.StringValue(instance.ConnectionString)
	state.Username = types.StringValue(instance.Username)

	// Map pgConfig
	if instance.PgConfig == nil {
		state.PgConfig = types.MapNull(types.StringType)
	} else {
		pgConfigValues := make(map[string]attr.Value, len(instance.PgConfig))
		for k, v := range instance.PgConfig {
			pgConfigValues[k] = types.StringValue(v)
		}
		state.PgConfig, _ = types.MapValue(types.StringType, pgConfigValues)
	}

	// Map pgBouncerConfig
	if instance.PgBouncerConfig == nil {
		state.PgBouncerConfig = types.MapNull(types.StringType)
	} else {
		pgBouncerConfigValues := make(map[string]attr.Value, len(instance.PgBouncerConfig))
		for k, v := range instance.PgBouncerConfig {
			pgBouncerConfigValues[k] = types.StringValue(v)
		}
		state.PgBouncerConfig, _ = types.MapValue(types.StringType, pgBouncerConfigValues)
	}

	// Map tags
	if len(instance.Tags) == 0 {
		state.Tags = types.MapNull(types.StringType)
	} else {
		tagsValues := make(map[string]attr.Value, len(instance.Tags))
		for _, tag := range instance.Tags {
			tagsValues[tag.Key] = types.StringValue(tag.Value)
		}
		state.Tags, _ = types.MapValue(types.StringType, tagsValues)
	}
}
