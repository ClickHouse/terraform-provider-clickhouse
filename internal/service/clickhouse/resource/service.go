// Configure adds the provider configured client to the resource.
func (r *ServiceResource) Configure(_ context.Context, req resource.ConfigureRequest, resp *resource.ConfigureResponse) {
	if req.ProviderData == nil {
		return
	}
	providerData, ok := req.ProviderData.(*service.ProviderData)
	if !ok {
		resp.Diagnostics.AddError("Unexpected provider data",
			fmt.Sprintf("expected *service.ProviderData, got %T. This is a bug in the provider.", req.ProviderData))
		return
	}
	if providerData.API == nil {
		resp.Diagnostics.AddError("ClickHouse Cloud API not configured",
			"This resource requires ClickHouse Cloud credentials. Set organization_id, token_key and token_secret on the provider (or the corresponding CLICKHOUSE_* environment variables).")
		return
	}
	r.client = providerData.API
}

// resolveIsHorizontal answers "is this service horizontal?" from the user's config — an explicit mode wins,
// otherwise the shape of the scaling fields decides, and on an existing service an omitted mode keeps whatever
// it already is. The precedence below spells that out.
// autoscaling_mode is Optional+Computed with no static default, so it is often absent from the config; the
// mode is then inferred from the scaling fields the config does set. Precedence:
//  1. an explicit autoscaling_mode token in the config wins;
//  2. otherwise a replica band with BOTH bounds present that isn't a fully-known equal band is a horizontal
//     signal — a distinct band (min != max) or a band with a bound still unknown (interpolated) — and it beats
//     keep-state. A half-band (one bound omitted) is NOT a signal here; the band's AlsoRequires rejects it, so
//     letting it fall through avoids stacking a spurious horizontal resolution on top of that error;
//  3. otherwise num_replicas is the vertical replica count — a present num_replicas signals vertical even on an
//     existing horizontal service (config-presence), so a horizontal config must not set num_replicas (a
//     module default that always injects it flips the service to vertical);
//  4. otherwise, on an existing service, keep the mode already in state — omitting an Optional+Computed field
//     means "keep current" (a resize/import, or a degenerate equal min == max band, which must NOT flip the
//     prior mode: only a distinct band or an explicit token switches);
//  5. otherwise (a fresh create with no horizontal signal — no band) default to vertical.
//
// The equal-band handling in 4/5 is about NOT flipping an existing service's mode, not about supporting an
// equal-band vertical config: a config that supplies an equal min == max band and resolves vertical is
// rejected at plan time (a vertical count must use num_replicas — the API reports vertical counts that way).
func resolveIsHorizontal(config, state models.ServiceResourceModel, hasState bool) bool {
	switch config.AutoscalingMode.ValueString() {
	case api.AutoscalingModeHorizontal:
		return true
	case api.AutoscalingModeVertical:
		return false
	}
	equalKnownBand := !config.MinReplicas.IsNull() && !config.MaxReplicas.IsNull() &&
		!config.MinReplicas.IsUnknown() && !config.MaxReplicas.IsUnknown() &&
		config.MinReplicas.ValueInt64() == config.MaxReplicas.ValueInt64()
	switch {
	case !config.MinReplicas.IsNull() && !config.MaxReplicas.IsNull() && !equalKnownBand:
		// Both bounds present and not a fully-known equal band: a distinct band (min != max) or a band with an
		// as-yet-unknown bound — an unambiguous horizontal signal that overrides the prior mode. A fully-known
		// equal band is excluded (the vertical count shape, and a degenerate horizontal one), so it defers to
		// keep-state / the vertical default. A half-band (one bound omitted) also falls through — the band's
		// AlsoRequires already rejects it, so resolving horizontal here would only stack a spurious diagnostic.
		return true
	case !config.NumReplicas.IsNull():
		return false
	case hasState:
		return state.AutoscalingMode.ValueString() == api.AutoscalingModeHorizontal
	default:
		return false
	}
}

func (r *ServiceResource) ModifyPlan(ctx context.Context, req resource.ModifyPlanRequest, resp *resource.ModifyPlanResponse) {
	if req.Plan.Raw.IsNull() {
		// If the entire plan is null, the resource is planned for destruction.
		return
	}

	var plan, state, config models.ServiceResourceModel
	diags := req.Plan.Get(ctx, &plan)
	resp.Diagnostics.Append(diags...)
	if !req.State.Raw.IsNull() {
		diags = req.State.Get(ctx, &state)
		resp.Diagnostics.Append(diags...)
	}
	if resp.Diagnostics.HasError() {
		return
	}

	if !req.Config.Raw.IsNull() {
		diags = req.Config.Get(ctx, &config)
		resp.Diagnostics.Append(diags...)
	}
	if resp.Diagnostics.HasError() {
		return
	}

	if !req.State.Raw.IsNull() {
		// Validations for updates.
		if !plan.BYOCID.IsNull() && !plan.BYOCID.IsUnknown() && plan.BYOCID != state.BYOCID {
			resp.Diagnostics.AddAttributeError(
				path.Root("byoc_id"),
				"Invalid Update",
				"ClickHouse does not support changing BYOC ID for a service",
			)
		}

		if plan.BackupID != state.BackupID {
			resp.Diagnostics.AddAttributeError(
				path.Root("backup_id"),
				"Invalid Update",
				"ClickHouse does not support changing Backup ID for a service",
			)
		}

		if !state.BackupID.IsNull() && plan.BackupID != state.BackupID {
			resp.Diagnostics.AddAttributeError(
				path.Root("backup_id"),
				"Invalid Update",
				"ClickHouse does not support changing Backup ID for a service",
			)
		}

		if !plan.CloudProvider.IsNull() && plan.CloudProvider != state.CloudProvider {
			resp.Diagnostics.AddAttributeError(
				path.Root("cloud_provider"),
				"Invalid Update",
				"ClickHouse does not support changing service cloud providers",
			)
		}

		if !plan.Region.IsNull() && plan.Region != state.Region {
			resp.Diagnostics.AddAttributeError(
				path.Root("region"),
				"Invalid Update",
				"ClickHouse does not support changing service regions",
			)
		}

		if plan.Tier != state.Tier {
			// Check if organization tier was changed from ppv1 to ppv2.
			if !plan.Tier.IsNull() && !plan.Tier.IsUnknown() {
				if state.Tier.IsNull() {
					// Plan specifies a tier, but the API returned null for it.
					// This means Organization was switched from ppv1 to ppv2,
					// so we ask the customer to remove the tier field from the .tf file.
					resp.Diagnostics.AddAttributeError(
						path.Root("tier"),
						"Action required",
						"Please remove the `tier` field from the service definition",
					)
				} else {
					// tier was changed in an organization using legacy tier, this is not allowed.
					resp.Diagnostics.AddAttributeError(
						path.Root("tier"),
						"Invalid Update",
						"ClickHouse does not support changing service tiers",
					)
				}
			}
		}

		if !config.EncryptionKey.IsNull() && plan.EncryptionKey != state.EncryptionKey {
			resp.Diagnostics.AddAttributeError(
				path.Root("encryption_key"),
				"Invalid Update",
				"ClickHouse does not support changing encryption_key",
			)
		}

		if !plan.EncryptionAssumedRoleIdentifier.IsNull() && plan.EncryptionAssumedRoleIdentifier != state.EncryptionAssumedRoleIdentifier {
			resp.Diagnostics.AddAttributeError(
				path.Root("encryption_assumed_role_identifier"),
				"Invalid Update",
				"ClickHouse does not support changing encryption_assumed_role_identifier",
			)
		}

		var isEnabled, wantEnabled bool
		{
			if !state.TransparentEncryptionData.IsNull() {
				stateTDE := models.TransparentEncryptionData{}
				state.TransparentEncryptionData.As(ctx, &stateTDE, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})
				isEnabled = stateTDE.Enabled.ValueBool()
			} else {
				isEnabled = false
			}

			if !config.TransparentEncryptionData.IsNull() && !config.TransparentEncryptionData.IsUnknown() {
				configTDE := models.TransparentEncryptionData{}
				config.TransparentEncryptionData.As(ctx, &configTDE, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})
				wantEnabled = configTDE.Enabled.ValueBool()
			} else {
				wantEnabled = false
			}
		}

		// Attempt to disable TDE.
		if isEnabled && !wantEnabled {
			resp.Diagnostics.AddAttributeError(
				path.Root("transparent_data_encryption.enabled"),
				"Invalid Update",
				"This service has TDE enabled, but your clickhouse_service resource is not setting it as enabled. Please ensure you have set the transparent_data_encryption.enabled attribute to true",
			)
		}

		// Attempt to enable TDE.
		if !isEnabled && wantEnabled {
			resp.Diagnostics.AddAttributeError(
				path.Root("transparent_data_encryption.enabled"),
				"Invalid Update",
				"It is not possible to enable TDE (Transparent data encryption) on an existing service.",
			)
		}

		if config.IdleTimeoutMinutes.IsNull() {
			plan.IdleTimeoutMinutes = types.Int64Null()
			resp.Plan.Set(ctx, plan)
		}

		if !state.IdleScaling.ValueBool() && plan.IdleScaling.ValueBool() {
			plan.IdleTimeoutMinutes = config.IdleTimeoutMinutes
		}
	}

	// Resolve the autoscaling mode for the plan-time checks below (see resolveIsHorizontal for the precedence).
	isHorizontal := resolveIsHorizontal(config, state, !req.State.Raw.IsNull())

	// These two mode-vs-field guards resolve mode from config, so an interpolated (Unknown) autoscaling_mode
	// can't be resolved yet — defer both to the API (mirroring the mode-switch nulling block below and the
	// scheduled validator), rather than guessing a mode and false-rejecting a config that resolves valid at
	// apply.
	modeKnown := !config.AutoscalingMode.IsUnknown()
	// Development tier rejects autoscaling outright in its own block below, so skip the mode-vs-field guards
	// for it — otherwise a dev config gets a doubled diagnostic, one of which steers to num_replicas (which
	// dev can't use either).
	enforceMode := modeKnown && plan.Tier.ValueString() != api.TierDevelopment

	// num_replicas is the vertical fixed replica count. The num_replicas↔band ConflictsWith validator doesn't
	// cover mode+num_replicas, so enforce the "forbidden when horizontal" contract here at plan time.
	if enforceMode && isHorizontal && !config.NumReplicas.IsNull() {
		resp.Diagnostics.AddError(
			"Invalid Configuration",
			"num_replicas can't be set when autoscaling_mode is \"horizontal\"",
		)
	}

	// Horizontal has fixed per-replica memory, not a total spread across replicas. The deprecated
	// min/max_total_memory_gb fields feed the legacy per-3-replicas /3 conversion on create, which would
	// silently mis-size a horizontal service — require the per-replica fields instead.
	if enforceMode && isHorizontal && (!config.MinTotalMemoryGb.IsNull() || !config.MaxTotalMemoryGb.IsNull()) {
		resp.Diagnostics.AddError(
			"Invalid Configuration",
			"the deprecated min_total_memory_gb/max_total_memory_gb can't be used with autoscaling_mode \"horizontal\"; set min_replica_memory_gb and max_replica_memory_gb (equal) instead",
		)
	}

	// The API reports every vertical service's replica count as num_replicas — a vertical min==max band is
	// collapsed on read (UC-1173) — so a vertical service configured via min_replicas/max_replicas can't
	// round-trip: the plan carries the band but the read returns num_replicas with the band cleared, an
	// inconsistent-result-after-apply. min_replicas/max_replicas are the horizontal band; a vertical count
	// must use num_replicas. A half-band (one bound set) also trips this and the band's AlsoRequires — but a
	// real terraform plan runs ValidateResourceConfig (AlsoRequires) first and stops there, so only the
	// direct-ModifyPlan unit tests see both.
	if enforceMode && !isHorizontal && (!config.MinReplicas.IsNull() || !config.MaxReplicas.IsNull()) {
		resp.Diagnostics.AddError(
			"Invalid Configuration",
			"min_replicas/max_replicas define a horizontal replica band; for vertical autoscaling set the replica count with num_replicas, or set autoscaling_mode = \"horizontal\" to use a band",
		)
	}

	if plan.Tier.ValueString() == api.TierDevelopment {
		if !plan.BYOCID.IsNull() && !plan.BYOCID.IsUnknown() {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"byoc_id cannot be defined if the service tier is development",
			)
		}

		if !plan.MinTotalMemoryGb.IsNull() {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"min_total_memory_gb cannot be defined if the service tier is development",
			)
		}

		if !plan.MaxTotalMemoryGb.IsNull() {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"max_total_memory_gb cannot be defined if the service tier is development",
			)
		}

		if !plan.NumReplicas.IsNull() && !plan.NumReplicas.IsUnknown() {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"num_replicas cannot be defined if the service tier is development",
			)
		}

		// Development tier has no autoscaling mode; reject an explicitly-set autoscaling_mode (either value)
		// or any band field. An explicit "vertical" would otherwise pass here but read back as null
		// (the non-production sync branch nulls autoscaling_mode) → inconsistent-result-after-apply.
		// These read the PLAN (not the config like the production branch's isHorizontal): dev-tier state is
		// always null for these fields, so UseStateForUnknown maps null→null in the plan, making plan and
		// config equivalent here — and num_replicas just above already keys off the plan, so this matches.
		if (!plan.AutoscalingMode.IsNull() && !plan.AutoscalingMode.IsUnknown()) ||
			(!plan.MinReplicas.IsNull() && !plan.MinReplicas.IsUnknown()) ||
			(!plan.MaxReplicas.IsNull() && !plan.MaxReplicas.IsUnknown()) {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"autoscaling_mode, min_replicas, and max_replicas cannot be defined if the service tier is development",
			)
		}

		if (!plan.MinReplicaMemoryGb.IsNull() && !plan.MinReplicaMemoryGb.IsUnknown()) ||
			(!plan.MaxReplicaMemoryGb.IsNull() && !plan.MaxReplicaMemoryGb.IsUnknown()) {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"min_replica_memory_gb and max_replica_memory_gb cannot be defined if the service tier is development",
			)
		}

		if (!plan.EncryptionKey.IsNull() && !plan.EncryptionKey.IsUnknown()) || !plan.EncryptionAssumedRoleIdentifier.IsNull() {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"encryption_key and encryption_assumed_role_identifier cannot be defined if the service tier is development",
			)
		}

		if !plan.BackupConfiguration.IsNull() && !plan.BackupConfiguration.IsUnknown() {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"backup_configuration cannot be defined if the service tier is development",
			)
		}

		if !plan.ReleaseChannel.IsUnknown() && plan.ReleaseChannel.ValueString() != api.ReleaseChannelDefault {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"release_channel must be 'default' if the service tier is development",
			)
		}
	} else {
		// Production and PPv2. Both autoscaling modes require the per-replica memory fields — vertical scales
		// memory across the [min, max] range, horizontal fixes it at min == max — so these requirements apply
		// regardless of mode. This is a create-time requirement keyed off the config: on a create the config
		// is the only memory source (no prior state), and reading the config distinguishes an omitted field
		// (Null → required error) from an interpolated one (Unknown → defer to apply, don't reject a valid
		// `= local.mem`). On an update the Computed fields keep their prior value, so the check doesn't apply.
		isCreate := req.State.Raw.IsNull()
		if !plan.BYOCID.IsNull() {
			if isCreate && config.MinReplicaMemoryGb.IsNull() {
				resp.Diagnostics.AddError(
					"Invalid Configuration",
					"min_replica_memory_gb must be defined if byoc_id is set",
				)
			}

			if isCreate && config.MaxReplicaMemoryGb.IsNull() {
				resp.Diagnostics.AddError(
					"Invalid Configuration",
					"max_replica_memory_gb must be defined if byoc_id is set",
				)
			}
		}

		if isCreate && config.MinReplicaMemoryGb.IsNull() && config.MinTotalMemoryGb.IsNull() {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"min_replica_memory_gb must be defined if the service tier is production or PPv2",
			)
		}

		if isCreate && config.MaxReplicaMemoryGb.IsNull() && config.MaxTotalMemoryGb.IsNull() {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"max_replica_memory_gb must be defined if the service tier is production or PPv2",
			)
		}

		if !plan.EncryptionKey.IsNull() && !plan.EncryptionKey.IsUnknown() && strings.Compare(plan.CloudProvider.ValueString(), "aws") != 0 {
			resp.Diagnostics.AddError(
				"Invalid Configuration",
				"encryption_key and the encryption_assumed_role_identifier is only available for AWS services",
			)
		}

		if !plan.BackupConfiguration.IsNull() && !plan.BackupConfiguration.IsUnknown() {
			bc := models.BackupConfiguration{}
			diag := plan.BackupConfiguration.As(ctx, &bc, basetypes.ObjectAsOptions{
				UnhandledNullAsEmpty:    false,
				UnhandledUnknownAsEmpty: false,
			})
			if diag.HasError() {
				resp.Diagnostics.Append(diag.Errors()...)
			} else {
				if !config.BackupConfiguration.IsNull() && !config.BackupConfiguration.IsUnknown() {
					cfgBackupConfig := models.BackupConfiguration{}
					diag := config.BackupConfiguration.As(ctx, &cfgBackupConfig, basetypes.ObjectAsOptions{
						UnhandledNullAsEmpty:    false,
						UnhandledUnknownAsEmpty: false,
					})
					if !diag.HasError() {
						if cfgBackupConfig.BackupStartTime.IsNull() || cfgBackupConfig.BackupStartTime.IsUnknown() {
							// Make BackupStartTime null if user only set BackupPeriodInHours.
							bc.BackupStartTime = types.StringNull()
							plan.BackupConfiguration = bc.ObjectValue()
							resp.Plan.Set(ctx, plan)
						}
					}
				}
			}
		}

		// CMEK->TDE migration.
		// if config.encryption_key is null, we need to wipe it out from the state even if the API returns it.
		// This happens when a service is migrated from CMEK to TDE.
		if config.EncryptionKey.IsNull() {
			if !state.EncryptionKey.IsNull() {
				plan.EncryptionKey = state.EncryptionKey
				resp.Plan.Set(ctx, plan)
			} else {
				plan.EncryptionKey = types.StringNull()
			}
		}

		// Mode-switch consistency: the Computed scaling fields are otherwise pinned to their prior value by
		// UseStateForUnknown, while syncServiceState rewrites them from the post-apply read — so a mode switch
		// that drops a field from the config plans the stale value but reads back the new mode's shape
		// ("inconsistent result after apply"). Write the resolved mode token and null the off-mode fields the
		// config no longer sets. Skip this when the config mode is Unknown (e.g. autoscaling_mode = a
		// not-yet-known reference): forcing a token there could contradict the value that resolves at apply.
		if !config.AutoscalingMode.IsUnknown() {
			if isHorizontal {
				plan.AutoscalingMode = types.StringValue(api.AutoscalingModeHorizontal)
				// num_replicas is the vertical fixed count; drop a value pinned from prior vertical state when
				// the config no longer sets it, so switching to horizontal doesn't carry a stale count.
				if config.NumReplicas.IsNull() {
					plan.NumReplicas = types.Int64Null()
				}
			} else {
				plan.AutoscalingMode = types.StringValue(api.AutoscalingModeVertical)
				// min/max_replicas are the horizontal band; drop a band pinned from prior horizontal state when
				// the config no longer sets it, so switching to vertical doesn't carry a stale band.
				if config.MinReplicas.IsNull() {
					plan.MinReplicas = types.Int64Null()
				}
				if config.MaxReplicas.IsNull() {
					plan.MaxReplicas = types.Int64Null()
				}
			}
			resp.Diagnostics.Append(resp.Plan.Set(ctx, plan)...)
		}
	}

	// A per-replica field counts as "set" only when it holds a concrete value: a null (e.g. a module passing
	// null) and an unknown (an omitted Optional+Computed per-replica field on a create, which UseStateForUnknown
	// leaves unknown) both mean "not set", so a deprecated-totals-only config must not trip these conflicts.
	perReplicaSet := func(v types.Int64) bool { return !v.IsNull() && !v.IsUnknown() }
	if !plan.MinTotalMemoryGb.IsNull() && perReplicaSet(plan.MinReplicaMemoryGb) {
		resp.Diagnostics.AddError(
			"Invalid Configuration",
			"min_total_memory_gb and min_replica_memory_gb can't be specified at the same time. Please remove deprecated field min_total_memory_gb",
		)
	}

	if !plan.MaxTotalMemoryGb.IsNull() && perReplicaSet(plan.MaxReplicaMemoryGb) {
		resp.Diagnostics.AddError(
			"Invalid Configuration",
			"max_total_memory_gb and max_replica_memory_gb can't be specified at the same time. Please remove deprecated field max_total_memory_gb",
		)
	}

	if (perReplicaSet(plan.MinReplicaMemoryGb) || perReplicaSet(plan.MaxReplicaMemoryGb)) && (!plan.MinTotalMemoryGb.IsNull() || !plan.MaxTotalMemoryGb.IsNull()) {
		resp.Diagnostics.AddError(
			"Invalid Configuration",
			"If you specify either min_replica_memory_gb or max_replica_memory_gb fields, you can't use deprecated min_total_memory_gb nor max_total_memory_gb fields any more.",
		)
	}

	if config.IdleTimeoutMinutes.IsNull() && !config.IdleScaling.IsNull() && config.IdleScaling.ValueBool() {
		resp.Diagnostics.AddError(
			"Invalid Configuration",
			"idle_timeout_minutes must be defined if idle_scaling is enabled",
		)
	}

	if !config.IdleTimeoutMinutes.IsNull() && !config.IdleScaling.IsNull() && !plan.IdleScaling.ValueBool() {
		resp.Diagnostics.AddError(
			"Invalid Configuration",
			"idle_timeout_minutes must be null if idle_scaling is disabled",
		)
	}

	if config.Endpoints.IsNull() || config.Endpoints.IsUnknown() {
		// User did not set the endpoints attribute
		if state.Endpoints.IsNull() || state.Endpoints.IsUnknown() {
			// State is not set, we leave the plan as-is as value is currently unknown.
		} else {
			// State is set and user didn't set (or removed) attribute from the service.
			endpoints := models.Endpoints{}
			diag := state.Endpoints.As(ctx, &endpoints, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})
			if diag.HasError() {
				return
			}

			mysql := models.OptionalEndpoint{}
			diag = endpoints.MySQL.As(ctx, &mysql, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})
			if diag.HasError() {
				return
			}

			if mysql.Enabled.ValueBool() {
				// Mysql protocol was enabled outside terraform.
				// The default is false, so in the plan we show the need to disable it.
				mysql.Enabled = types.BoolValue(false)
				mysql.Host = types.StringNull()
				mysql.Port = types.Int32Null()
				endpoints.MySQL = mysql.ObjectValue()
				plan.Endpoints = endpoints.ObjectValue()

			} else {
				// All good, no change so we copy Endpoints from the state to the Plan to show no changes.
				plan.Endpoints = state.Endpoints
			}

			resp.Plan.Set(ctx, plan)
		}
	} else {
		// User has Endpoint config set

		var wantEnabled bool
		{
			endpoints := models.Endpoints{}
			diag := config.Endpoints.As(ctx, &endpoints, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})
			if diag.HasError() {
				return
			}

			mysql := models.OptionalEndpoint{}
			diag = endpoints.MySQL.As(ctx, &mysql, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})
			if diag.HasError() {
				return
			}

			wantEnabled = mysql.Enabled.ValueBool()
		}

		var isEnabled bool
		if !req.State.Raw.IsNull() {
			endpoints := models.Endpoints{}
			diag := state.Endpoints.As(ctx, &endpoints, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})
			if diag.HasError() {
				return
			}

			mysql := models.OptionalEndpoint{}
			diag = endpoints.MySQL.As(ctx, &mysql, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})
			if diag.HasError() {
				return
			}

			isEnabled = mysql.Enabled.ValueBool()
		}

		if wantEnabled && isEnabled {
			// User did not change wantEnabled so there is no reason to change anything in the Endpoints field.
			plan.Endpoints = state.Endpoints
			resp.Plan.Set(ctx, plan)
		}
	}

	{
		// Default value if there is no TDE attribute in the state nor in the config.
		tde := models.TransparentEncryptionData{
			Enabled: types.BoolValue(false),
		}

		// Read the TDE config if present to check if TDE is enabled or not.
		if !config.TransparentEncryptionData.IsNull() {
			configTDE := models.TransparentEncryptionData{}
			config.TransparentEncryptionData.As(ctx, &configTDE, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})

			tde.Enabled = configTDE.Enabled
			tde.RoleID = types.StringUnknown()
		}

		// Read the Role ID from the state if set
		if !req.State.Raw.IsNull() && !state.TransparentEncryptionData.IsNull() {
			stateTDE := models.TransparentEncryptionData{}
			state.TransparentEncryptionData.As(ctx, &stateTDE, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})

			tde.RoleID = stateTDE.RoleID
		}

		plan.TransparentEncryptionData = tde.ObjectValue()
		resp.Plan.Set(ctx, plan)

	}

	if !config.DataWarehouseID.IsNull() {
		if !config.BackupConfiguration.IsNull() {
			resp.Diagnostics.AddError(
				"Invalid configuration",
				"backup_configuration cannot be specified when warehouse_id is set",
			)
		}
		if !state.BackupConfiguration.IsNull() {
			resp.Diagnostics.AddError(
				"Invalid state for service",
				"backup_configuration cannot co-exist in terraform state for a service with data_warehouse_id set",
			)
		}
		plan.BackupConfiguration = types.ObjectNull(models.BackupConfiguration{}.ObjectType().AttrTypes)
		resp.Plan.Set(ctx, plan)
	}
}

// ValidateConfig applies the cheap, mode-agnostic ordering checks that give a clearer plan-time error than
// the API's contradiction responses. The mode ↔ field combination rules — which fields each autoscaling_mode
// accepts, num_replicas vs the band, ranged vs fixed per-replica memory — are owned by the API (UC-1173) and
// deliberately not duplicated here. Runs at plan time for both create and update.
func (r *ServiceResource) ValidateConfig(ctx context.Context, req resource.ValidateConfigRequest, resp *resource.ValidateConfigResponse) {
	var config models.ServiceResourceModel
	resp.Diagnostics.Append(req.Config.Get(ctx, &config)...)
	if resp.Diagnostics.HasError() {
		return
	}

	known := func(v types.Int64) bool { return !v.IsNull() && !v.IsUnknown() }

	if known(config.MinReplicas) && known(config.MaxReplicas) &&
		config.MinReplicas.ValueInt64() > config.MaxReplicas.ValueInt64() {
		resp.Diagnostics.AddAttributeError(
			path.Root("min_replicas"),
			"Invalid Configuration",
			"min_replicas must be less than or equal to max_replicas.",
		)
	}

	if known(config.MinReplicaMemoryGb) && known(config.MaxReplicaMemoryGb) &&
		config.MinReplicaMemoryGb.ValueInt64() > config.MaxReplicaMemoryGb.ValueInt64() {
		resp.Diagnostics.AddAttributeError(
			path.Root("min_replica_memory_gb"),
			"Invalid Configuration",
			"min_replica_memory_gb must be less than or equal to max_replica_memory_gb.",
		)
	}
}

// Create a new resource
func (r *ServiceResource) Create(ctx context.Context, req resource.CreateRequest, resp *resource.CreateResponse) {
	// Retrieve values from plan and config
	var plan, config models.ServiceResourceModel
	diags := req.Plan.Get(ctx, &plan)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}
	diags = req.Config.Get(ctx, &config)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Generate API request body from plan
	service := api.Service{
		Name:     plan.Name.ValueString(),
		Provider: plan.CloudProvider.ValueString(),
		Region:   plan.Region.ValueString(),
		Tier:     plan.Tier.ValueString(),
	}

	if !plan.BYOCID.IsUnknown() && !plan.BYOCID.IsNull() {
		service.BYOCId = plan.BYOCID.ValueStringPointer()
	}

	if !plan.BackupID.IsUnknown() && !plan.BackupID.IsNull() {
		service.BackupID = plan.BackupID.ValueStringPointer()
	}

	if !plan.ReleaseChannel.IsUnknown() && !plan.ReleaseChannel.IsNull() {
		service.ReleaseChannel = plan.ReleaseChannel.ValueString()
	}

	if !plan.DataWarehouseID.IsUnknown() && !plan.DataWarehouseID.IsNull() {
		service.DataWarehouseId = plan.DataWarehouseID.ValueStringPointer()
		service.ReadOnly = plan.ReadOnly.ValueBool()
	}

	if service.Tier == api.TierProduction || service.Tier == api.TierPPv2 {
		// Pass the explicit autoscaling_mode and scaling fields straight through to the API (UC-1173), which
		// validates the mode/field combination. Both modes use min/max_replicas and the per-replica memory
		// fields — vertical scales memory across the [min, max] range at a fixed replica count, horizontal
		// scales replicas across the band at fixed (min == max) per-replica memory — so no mode branch is
		// needed here; send whatever is set and let the API reject an invalid combination.
		if !plan.AutoscalingMode.IsNull() && !plan.AutoscalingMode.IsUnknown() {
			service.AutoscalingMode = plan.AutoscalingMode.ValueString()
		}
		if !plan.MinReplicas.IsNull() && !plan.MinReplicas.IsUnknown() {
			minReplicas := int(plan.MinReplicas.ValueInt64())
			service.MinReplicas = &minReplicas
		}
		if !plan.MaxReplicas.IsNull() && !plan.MaxReplicas.IsUnknown() {
			maxReplicas := int(plan.MaxReplicas.ValueInt64())
			service.MaxReplicas = &maxReplicas
		}
		if !plan.NumReplicas.IsNull() && !plan.NumReplicas.IsUnknown() {
			numReplicas := int(plan.NumReplicas.ValueInt64())
			if numReplicas > 0 {
				service.NumReplicas = &numReplicas
			}
		}

		var minReplicaMemoryGb, maxReplicaMemoryGb int
		// Use the per-replica value when it's actually set: an explicitly-null min_replica_memory_gb
		// (common when a module passes null) is NOT "set" — fall back to translating the deprecated total.
		if !plan.MinReplicaMemoryGb.IsNull() && !plan.MinReplicaMemoryGb.IsUnknown() {
			minReplicaMemoryGb = int(plan.MinReplicaMemoryGb.ValueInt64())
		} else {
			// Due to a bug on the API, we always assumed the MinTotalMemoryGb value was always related to 3 replicas.
			// Now we use a per-replica API to set the min total memory so we need to divide by 3 to get the same
			// behaviour as before.
			minReplicaMemoryGb = int(plan.MinTotalMemoryGb.ValueInt64() / 3)
		}

		if !plan.MaxReplicaMemoryGb.IsNull() && !plan.MaxReplicaMemoryGb.IsUnknown() {
			maxReplicaMemoryGb = int(plan.MaxReplicaMemoryGb.ValueInt64())
		} else {
			// Due to a bug on the API, we always assumed the MaxTotalMemoryGb value was always related to 3 replicas.
			// Now we use a per-replica API to set the min total memory so we need to divide by 3 to get the same
			// behaviour as before.
			maxReplicaMemoryGb = int(plan.MaxTotalMemoryGb.ValueInt64() / 3)
		}

		service.MinReplicaMemoryGb = &minReplicaMemoryGb
		service.MaxReplicaMemoryGb = &maxReplicaMemoryGb

		if !plan.EncryptionKey.IsNull() && !plan.EncryptionKey.IsUnknown() {
			service.EncryptionKey = plan.EncryptionKey.ValueString()
		}
		if !plan.EncryptionAssumedRoleIdentifier.IsNull() {
			service.EncryptionAssumedRoleIdentifier = plan.EncryptionAssumedRoleIdentifier.ValueString()
		}
	}

	if !plan.TransparentEncryptionData.IsNull() {
		tde := &models.TransparentEncryptionData{}
		diag := plan.TransparentEncryptionData.As(ctx, tde, basetypes.ObjectAsOptions{UnhandledNullAsEmpty: false, UnhandledUnknownAsEmpty: false})
		if diag.HasError() {
			return
		}
		service.HasTransparentDataEncryption = tde.Enabled.ValueBool()
	}

	service.IdleScaling = plan.IdleScaling.ValueBool()
	if !plan.IdleTimeoutMinutes.IsNull() && !plan.IdleTimeoutMinutes.IsUnknown() {
		idleTimeoutMinutes := int(plan.IdleTimeoutMinutes.ValueInt64())
		service.IdleTimeoutMinutes = &idleTimeoutMinutes
	}

	ipAccessModels := make([]models.IPAccessList, 0, len(plan.IpAccessList.Elements()))
	plan.IpAccessList.ElementsAs(ctx, &ipAccessModels, false)
	ipAccessLists := make([]api.IpAccess, 0, len(ipAccessModels))
	for _, ipAccessModel := range ipAccessModels {
		ipAccessLists = append(ipAccessLists, api.IpAccess{
			Source:      ipAccessModel.Source.ValueString(),
			Description: ipAccessModel.Description.ValueString(),
		})
	}
	service.IpAccessList = ipAccessLists

	if !plan.Tags.IsUnknown() && !plan.Tags.IsNull() {
		tagsMap := make(map[string]string)
		if diag := plan.Tags.ElementsAs(ctx, &tagsMap, false); diag.HasError() {
			resp.Diagnostics.Append(diag...)
			return
		}

		tags := make([]api.Tag, 0, len(tagsMap))
		for key, value := range tagsMap {
			tags = append(tags, api.Tag{
				Key:   key,
				Value: value,
			})
		}
		service.Tags = tags
	}

	// Endpoints
	if !plan.Endpoints.IsNull() && !plan.Endpoints.IsUnknown() {
		endpointsConfig := models.Endpoints{}
		diag := plan.Endpoints.As(ctx, &endpointsConfig, basetypes.ObjectAsOptions{
			UnhandledNullAsEmpty:    false,
			UnhandledUnknownAsEmpty: false,
		})
		if diag.HasError() {
			resp.Diagnostics.Append(diag.Errors()...)
			return
		}

		if !endpointsConfig.MySQL.IsNull() {
			mysql := models.OptionalEndpoint{}
			diag = endpointsConfig.MySQL.As(ctx, &mysql, basetypes.ObjectAsOptions{
				UnhandledNullAsEmpty:    false,
				UnhandledUnknownAsEmpty: false,
			})
			if diag.HasError() {
				resp.Diagnostics.Append(diag.Errors()...)
				return
			}

			if mysql.Enabled.ValueBool() {
				service.Endpoints = append(service.Endpoints, api.Endpoint{
					Protocol: api.EndpointProtocolMysql,
					Enabled:  true,
				})
			}
		}
	}

	if !plan.ComplianceType.IsUnknown() && !plan.ComplianceType.IsNull() {
		service.ComplianceType = plan.ComplianceType.ValueStringPointer()
	}

	if !plan.EnableCoreDumps.IsNull() && !plan.EnableCoreDumps.IsUnknown() {
		service.EnableCoreDumps = plan.EnableCoreDumps.ValueBoolPointer()
	}

	// Create new service
	s, _, err := r.client.CreateService(ctx, service)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error creating service",
			"Could not create service, unexpected error: "+err.Error(),
		)
		return
	}

	err = r.client.WaitForServiceState(ctx, s.Id, func(state string) bool { return state != api.StateProvisioning }, 90*60)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error retrieving service state",
			"Could not retrieve service state after creation, unexpected error: "+err.Error(),
		)
		return
	}

	// Password and backup settings are only set on parent instances for hydra services
	if plan.DataWarehouseID.IsUnknown() || plan.DataWarehouseID.IsNull() {
		// Update service password if provided explicitly (prefer password_wo over password)
		if password, passwordWO := plan.Password.ValueString(), config.PasswordWO.ValueString(); len(password) > 0 || len(passwordWO) > 0 {
			if len(passwordWO) > 0 {
				password = passwordWO
			}
			_, err := r.client.UpdateServicePassword(ctx, s.Id, servicePasswordUpdateFromPlainPassword(password))
			if err != nil {
				resp.Diagnostics.AddError(
					"Error setting service password",
					"Could not set service password after creation, unexpected error: "+err.Error(),
				)
				return
			}
		}

		// Update hashed service password if provided explicitly
		if passwordHash, doubleSha1PasswordHash := plan.PasswordHash.ValueString(), plan.DoubleSha1PasswordHash.ValueString(); len(passwordHash) > 0 || len(doubleSha1PasswordHash) > 0 {
			passwordUpdate := api.ServicePasswordUpdate{
				NewPasswordHash: passwordHash,
			}

			if len(doubleSha1PasswordHash) > 0 {
				passwordUpdate.NewDoubleSha1Hash = doubleSha1PasswordHash
			}

			_, err := r.client.UpdateServicePassword(ctx, s.Id, passwordUpdate)
			if err != nil {
				resp.Diagnostics.AddError(
					"Error setting service password",
					"Could not set service password after creation, unexpected error: "+err.Error(),
				)
				return
			}
		}

		// Set backup settings.
		if !plan.BackupConfiguration.IsNull() && !plan.BackupConfiguration.IsUnknown() {
			bc := models.BackupConfiguration{}
			diag := plan.BackupConfiguration.As(ctx, &bc, basetypes.ObjectAsOptions{
				UnhandledNullAsEmpty:    false,
				UnhandledUnknownAsEmpty: false,
			})
			if diag.HasError() {
				return
			}

			var startTime *string
			if !bc.BackupStartTime.IsUnknown() {
				startTime = bc.BackupStartTime.ValueStringPointer()
			}

			_, err = r.client.UpdateBackupConfiguration(ctx, s.Id, api.BackupConfiguration{
				BackupPeriodInHours:          bc.BackupPeriodInHours.ValueInt32Pointer(),
				BackupRetentionPeriodInHours: bc.BackupRetentionPeriodInHours.ValueInt32Pointer(),
				BackupStartTime:              startTime,
			})
			if err != nil {
				resp.Diagnostics.AddError(
					"Error setting service backup configuration",
					"Could not set service backup settings after creation, unexpected error: "+err.Error(),
				)
				return
			}
		}
	}

	// Set query api endpoints
	if !plan.QueryAPIEndpoints.IsNull() {
		qae := models.QueryAPIEndpoints{}
		diag := plan.QueryAPIEndpoints.As(ctx, &qae, basetypes.ObjectAsOptions{
			UnhandledNullAsEmpty:    false,
			UnhandledUnknownAsEmpty: false,
		})
		if diag.HasError() {
			resp.Diagnostics.Append(diag.Errors()...)
			return
		}

		roles := make([]string, 0)
		diag = qae.Roles.ElementsAs(ctx, &roles, false)
		if diag.HasError() {
			resp.Diagnostics.Append(diag.Errors()...)
			return
		}

		keys := make([]string, 0)
		diag = qae.APIKeyIDs.ElementsAs(ctx, &keys, false)
		if diag.HasError() {
			resp.Diagnostics.Append(diag.Errors()...)
			return
		}

		_, err := r.client.CreateQueryEndpoint(ctx, s.Id, api.ServiceQueryEndpoint{
			Roles:          roles,
			OpenApiKeys:    keys,
			AllowedOrigins: qae.AllowedOrigins.ValueString(),
		})
		if err != nil {
			resp.Diagnostics.AddError(
				"Error setting service query API endpoints",
				"Could not set service query API endpoints, unexpected error: "+err.Error(),
			)
			return
		}
	}

	// Map response body to schema and populate Computed attribute values
	plan.ID = types.StringValue(s.Id)
	err = r.syncServiceState(ctx, &plan, true)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Reading ClickHouse Service",
			"Could not read ClickHouse service id "+plan.ID.ValueString()+": "+err.Error(),
		)
		return
	}

	// Set state to fully populated data
	diags = resp.State.Set(ctx, plan)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}
}

// Read refreshes the Terraform state with the latest data.
func (r *ServiceResource) Read(ctx context.Context, req resource.ReadRequest, resp *resource.ReadResponse) {
	var state models.ServiceResourceModel
	diags := req.State.Get(ctx, &state)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	err := r.syncServiceState(ctx, &state, false)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Reading ClickHouse Service",
			"Could not read ClickHouse service id "+state.ID.ValueString()+": "+err.Error(),
		)
		return
	}

	if state.ID.IsNull() {
		// Resource was deleted outside terraform
		resp.State.RemoveResource(ctx)
	} else {
		// Set refreshed state
		diags = resp.State.Set(ctx, state)
		resp.Diagnostics.Append(diags...)
	}
}

// Update updates the resource and sets the updated Terraform state on success.
func (r *ServiceResource) Update(ctx context.Context, req resource.UpdateRequest, resp *resource.UpdateResponse) {
	// Retrieve values from plan
	var plan, state, config models.ServiceResourceModel
	diags := req.Plan.Get(ctx, &plan)
	resp.Diagnostics.Append(diags...)
	diags = req.State.Get(ctx, &state)
	resp.Diagnostics.Append(diags...)
	resp.Diagnostics.Append(req.Config.Get(ctx, &config)...)

	if resp.Diagnostics.HasError() {
		return
	}

	// Generate API request body from plan
	serviceId := state.ID.ValueString()
	service := api.ServiceUpdate{
		Name:         "",
		IpAccessList: nil,
	}
	serviceChange := false

	if plan.Name != state.Name {
		service.Name = plan.Name.ValueString()
		serviceChange = true
	}

	if plan.ReleaseChannel != state.ReleaseChannel && !plan.ReleaseChannel.IsUnknown() {
		service.ReleaseChannel = plan.ReleaseChannel.ValueString()
		serviceChange = true
	}

	if !plan.IpAccessList.Equal(state.IpAccessList) {
		serviceChange = true
		var currentIPAccessList, desiredIPAccessList []models.IPAccessList
		state.IpAccessList.ElementsAs(ctx, &currentIPAccessList, false)
		plan.IpAccessList.ElementsAs(ctx, &desiredIPAccessList, false)

		var add, remove []api.IpAccess

		for _, ipAccess := range currentIPAccessList {
			remove = append(remove, api.IpAccess{
				Source:      ipAccess.Source.ValueString(),
				Description: ipAccess.Description.ValueString(),
			})
		}

		for _, ipAccess := range desiredIPAccessList {
			add = append(add, api.IpAccess{
				Source:      ipAccess.Source.ValueString(),
				Description: ipAccess.Description.ValueString(),
			})
		}

		service.IpAccessList = &api.IpAccessUpdate{
			Add:    add,
			Remove: remove,
		}
	}

	if !plan.Tags.Equal(state.Tags) {
		currentTagsMap := make(map[string]string)
		desiredTagsMap := make(map[string]string)

		if !state.Tags.IsNull() {
			if diags := state.Tags.ElementsAs(ctx, &currentTagsMap, false); diags.HasError() {
				resp.Diagnostics.Append(diags...)
				return
			}
		}

		if !plan.Tags.IsNull() {
			if diags := plan.Tags.ElementsAs(ctx, &desiredTagsMap, false); diags.HasError() {
				resp.Diagnostics.Append(diags...)
				return
			}
		}

		add, remove := computeTagChanges(currentTagsMap, desiredTagsMap)

		if len(add) > 0 || len(remove) > 0 {
			serviceChange = true
			service.Tags = &api.TagUpdate{
				Add:    add,
				Remove: remove,
			}
		}
	}

	// Endpoints
	if !plan.Endpoints.Equal(state.Endpoints) {
		serviceChange = true
		if !plan.Endpoints.IsNull() && !plan.Endpoints.IsUnknown() {
			endpointsConfig := models.Endpoints{}
			diag := plan.Endpoints.As(ctx, &endpointsConfig, basetypes.ObjectAsOptions{
				UnhandledNullAsEmpty:    false,
				UnhandledUnknownAsEmpty: false,
			})
			if diag.HasError() {
				resp.Diagnostics.Append(diag.Errors()...)
				return
			}

			if !endpointsConfig.MySQL.IsNull() && !endpointsConfig.MySQL.IsUnknown() {
				mysql := models.OptionalEndpoint{}
				diag = endpointsConfig.MySQL.As(ctx, &mysql, basetypes.ObjectAsOptions{
					UnhandledNullAsEmpty:    false,
					UnhandledUnknownAsEmpty: false,
				})
				if diag.HasError() {
					resp.Diagnostics.Append(diag.Errors()...)
					return
				}

				if mysql.Enabled.ValueBool() {
					service.Endpoints = append(service.Endpoints, api.Endpoint{
						Protocol: api.EndpointProtocolMysql,
						Enabled:  true,
					})
				} else {
					// MySQL endpoint was enabled, but now the enabled flag was set to false.
					// We need to disable mysql endpoint.
					service.Endpoints = append(service.Endpoints, api.Endpoint{
						Protocol: api.EndpointProtocolMysql,
						Enabled:  false,
					})
				}
			} else {
				// MySQL endpoint was enabled, but now the 'mysql' attribute was removed.
				// We need to disable mysql endpoint.
				service.Endpoints = append(service.Endpoints, api.Endpoint{
					Protocol: api.EndpointProtocolMysql,
					Enabled:  false,
				})
			}
		} else {
			// MySQL endpoint was enabled, but now the whole `endpoints_configuration` attribute was removed.
			// We need to disable mysql endpoint.
			service.Endpoints = append(service.Endpoints, api.Endpoint{
				Protocol: api.EndpointProtocolMysql,
				Enabled:  false,
			})
		}
	}

	if plan.EnableCoreDumps != state.EnableCoreDumps {
		serviceChange = true
		service.EnableCoreDumps = plan.EnableCoreDumps.ValueBoolPointer()
	}

	// Update existing service
	if serviceChange {
		var err error
		_, err = r.client.UpdateService(ctx, serviceId, service)
		if err != nil {
			resp.Diagnostics.AddError(
				"Error Updating ClickHouse Service",
				"Could not update service, unexpected error: "+err.Error(),
			)
			return
		}
	}

	scalingChange := false
	replicaScaling := api.ReplicaScalingUpdate{
		IdleScaling: state.IdleScaling.ValueBoolPointer(),
	}

	if plan.IdleScaling != state.IdleScaling {
		scalingChange = true
		idleScaling := new(bool)
		*idleScaling = plan.IdleScaling.ValueBool()
		replicaScaling.IdleScaling = idleScaling
	}
	// Include the plan's resolved autoscaling_mode (including a token ModifyPlan wrote) in the PATCH body so
	// the API applies the mode rather than inferring it from field presence (UC-1173). A mode difference also
	// forces the PATCH; when only another scaling field changed, the mode rides along on that PATCH.
	if !plan.AutoscalingMode.IsNull() && !plan.AutoscalingMode.IsUnknown() {
		replicaScaling.AutoscalingMode = plan.AutoscalingMode.ValueStringPointer()
		if plan.AutoscalingMode != state.AutoscalingMode {
			scalingChange = true
		}
	}
	if plan.MinTotalMemoryGb != state.MinTotalMemoryGb {
		scalingChange = true
		if !plan.MinTotalMemoryGb.IsNull() {
			// Due to a bug on the API, we always assumed the MinTotalMemoryGb value was always related to 3 replicas.
			// Now we use a per-replica API to set the min total memory so we need to divide by 3 to get the same
			// behaviour as before.
			minTotalMemoryGb := int(plan.MinTotalMemoryGb.ValueInt64()) / 3
			replicaScaling.MinReplicaMemoryGb = &minTotalMemoryGb
		}
	}
	if plan.MaxTotalMemoryGb != state.MaxTotalMemoryGb {
		scalingChange = true
		if !plan.MaxTotalMemoryGb.IsNull() {
			// Due to a bug on the API, we always assumed the MaxTotalMemoryGb value was always related to 3 replicas.
			// Now we use a per-replica API to set the min total memory so we need to divide by 3 to get the same
			// behaviour as before.
			maxTotalMemoryGb := int(plan.MaxTotalMemoryGb.ValueInt64()) / 3
			replicaScaling.MaxReplicaMemoryGb = &maxTotalMemoryGb
		}
	}
	if !plan.MinReplicaMemoryGb.IsUnknown() && plan.MinReplicaMemoryGb != state.MinReplicaMemoryGb {
		scalingChange = true
		if !plan.MinReplicaMemoryGb.IsNull() {
			minReplicaMemoryGb := int(plan.MinReplicaMemoryGb.ValueInt64())
			replicaScaling.MinReplicaMemoryGb = &minReplicaMemoryGb
		}
	}
	if !plan.MaxReplicaMemoryGb.IsUnknown() && plan.MaxReplicaMemoryGb != state.MaxReplicaMemoryGb {
		scalingChange = true
		if !plan.MaxReplicaMemoryGb.IsNull() {
			maxReplicaMemoryGb := int(plan.MaxReplicaMemoryGb.ValueInt64())
			replicaScaling.MaxReplicaMemoryGb = &maxReplicaMemoryGb
		}
	}
	if !plan.NumReplicas.IsUnknown() && plan.NumReplicas != state.NumReplicas {
		scalingChange = true

		if !plan.NumReplicas.IsNull() {
			numReplicas := int(plan.NumReplicas.ValueInt64())
			replicaScaling.NumReplicas = &numReplicas
		}
	}
	// On a mode switch ModifyPlan nulls the fields the target mode doesn't use, so the `!plan.X.IsNull()` guards
	// here leave them out of the PATCH (omitempty) — the mode plus the set fields are the switch signal sent.
	// The API clears the dropped fields server-side: both modes write the same min/max replica + per-replica
	// memory storage (num_replicas is derived on read, not stored), and the switch requires the new mode's
	// fields, so sending them overwrites the old shape — no explicit nulls needed (confirmed against UC-1173).
	if !plan.MinReplicas.IsUnknown() && plan.MinReplicas != state.MinReplicas {
		scalingChange = true
		if !plan.MinReplicas.IsNull() {
			minReplicas := int(plan.MinReplicas.ValueInt64())
			replicaScaling.MinReplicas = &minReplicas
		}
	}
	if !plan.MaxReplicas.IsUnknown() && plan.MaxReplicas != state.MaxReplicas {
		scalingChange = true
		if !plan.MaxReplicas.IsNull() {
			maxReplicas := int(plan.MaxReplicas.ValueInt64())
			replicaScaling.MaxReplicas = &maxReplicas
		}
	}
	if plan.IdleTimeoutMinutes != state.IdleTimeoutMinutes {
		scalingChange = true
		if !plan.IdleTimeoutMinutes.IsNull() {
			idleTimeoutMinutes := int(plan.IdleTimeoutMinutes.ValueInt64())
			replicaScaling.IdleTimeoutMinutes = &idleTimeoutMinutes
		}
	}

	if scalingChange {
		var err error
		_, err = r.client.UpdateReplicaScaling(ctx, serviceId, replicaScaling)
		if err != nil {
			resp.Diagnostics.AddError(
				"Error Updating ClickHouse Service Scaling",
				"Could not update service scaling, unexpected error: "+err.Error(),
			)
			return
		}
	}

	// Handle write-only password attribute (preferred) - update when version changes
	if !plan.PasswordWOVersion.IsNull() && plan.PasswordWOVersion != state.PasswordWOVersion {
		if passwordWO := config.PasswordWO.ValueString(); len(passwordWO) > 0 {
			_, err := r.client.UpdateServicePassword(ctx, serviceId, servicePasswordUpdateFromPlainPassword(passwordWO))
			if err != nil {
				resp.Diagnostics.AddError(
					"Error Updating ClickHouse Service Password",
					"Could not update service password, unexpected error: "+err.Error(),
				)
				return
			}
		}
	} else if password := plan.Password.ValueString(); len(password) > 0 && plan.Password != state.Password {
		_, err := r.client.UpdateServicePassword(ctx, serviceId, servicePasswordUpdateFromPlainPassword(password))
		if err != nil {
			resp.Diagnostics.AddError(
				"Error Updating ClickHouse Service Password",
				"Could not update service password, unexpected error: "+err.Error(),
			)
			return
		}
	} else if !plan.PasswordHash.IsNull() || !plan.DoubleSha1PasswordHash.IsNull() {
		passwordUpdate := api.ServicePasswordUpdate{}

		if !plan.PasswordHash.IsNull() { // change in password hash
			passwordUpdate.NewPasswordHash = plan.PasswordHash.ValueString()
		} else { // no change in password hash, use state value
			passwordUpdate.NewPasswordHash = state.PasswordHash.ValueString()
		}

		if !plan.DoubleSha1PasswordHash.IsNull() { // change in double sha1 password hash
			passwordUpdate.NewDoubleSha1Hash = plan.DoubleSha1PasswordHash.ValueString()
		}

		_, err := r.client.UpdateServicePassword(ctx, serviceId, passwordUpdate)
		if err != nil {
			resp.Diagnostics.AddError(
				"Error Updating ClickHouse Service Password",
				"Could not update service password, unexpected error: "+err.Error(),
			)
			return
		}
	}

	// Update Query API endpoints settings.
	if !plan.QueryAPIEndpoints.IsNull() {
		qae := models.QueryAPIEndpoints{}
		diag := plan.QueryAPIEndpoints.As(ctx, &qae, basetypes.ObjectAsOptions{
			UnhandledNullAsEmpty:    false,
			UnhandledUnknownAsEmpty: false,
		})
		if diag.HasError() {
			resp.Diagnostics.Append(diag.Errors()...)
			return
		}

		roles := make([]string, 0)
		diag = qae.Roles.ElementsAs(ctx, &roles, false)
		if diag.HasError() {
			resp.Diagnostics.Append(diag.Errors()...)
			return
		}

		keys := make([]string, 0)
		diag = qae.APIKeyIDs.ElementsAs(ctx, &keys, false)
		if diag.HasError() {
			resp.Diagnostics.Append(diag.Errors()...)
			return
		}

		_, err := r.client.CreateQueryEndpoint(ctx, serviceId, api.ServiceQueryEndpoint{
			Roles:          roles,
			OpenApiKeys:    keys,
			AllowedOrigins: qae.AllowedOrigins.ValueString(),
		})
		if err != nil {
			resp.Diagnostics.AddError(
				"Error setting service query API endpoints",
				"Could not set service query API endpoints, unexpected error: "+err.Error(),
			)
			return
		}
	} else if !state.QueryAPIEndpoints.IsNull() {
		// query_api_endpoints was removed from tf file
		// or there was an external change
		err := r.client.DeleteQueryEndpoint(ctx, serviceId)
		if err != nil {
			resp.Diagnostics.AddError(
				"Error deleting service query API endpoints",
				"Could not delete service query API endpoints, unexpected error: "+err.Error(),
			)
			return
		}
	}

	// Set backup settings.
	{
		if !plan.BackupConfiguration.IsNull() && !plan.BackupConfiguration.IsUnknown() && !plan.BackupConfiguration.Equal(state.BackupConfiguration) {
			bc := models.BackupConfiguration{}
			diag := plan.BackupConfiguration.As(ctx, &bc, basetypes.ObjectAsOptions{
				UnhandledNullAsEmpty:    false,
				UnhandledUnknownAsEmpty: false,
			})
			if diag.HasError() {
				resp.Diagnostics.Append(diag.Errors()...)
				return
			}

			backupConfig := api.BackupConfiguration{
				BackupPeriodInHours:          nil,
				BackupRetentionPeriodInHours: nil,
				BackupStartTime:              nil,
			}

			if !bc.BackupPeriodInHours.IsUnknown() {
				backupConfig.BackupPeriodInHours = bc.BackupPeriodInHours.ValueInt32Pointer()
			}

			if !bc.BackupRetentionPeriodInHours.IsUnknown() {
				backupConfig.BackupRetentionPeriodInHours = bc.BackupRetentionPeriodInHours.ValueInt32Pointer()
			}

			if !bc.BackupStartTime.IsUnknown() {
				backupConfig.BackupStartTime = bc.BackupStartTime.ValueStringPointer()
			}

			_, err := r.client.UpdateBackupConfiguration(ctx, serviceId, backupConfig)
			if err != nil {
				resp.Diagnostics.AddError(
					"Error setting service backup configuration",
					"Could not update service backup settings, unexpected error: "+err.Error(),
				)
				return
			}
		}
	}

	err := r.syncServiceState(ctx, &plan, true)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error syncing service state",
			"Could not sync service state, unexpected error: "+err.Error(),
		)
		return
	}

	// CMEK->TDE migration.
	// if config.encryption_key is null, we need to wipe it out from the state even if the API returns it.
	// This happens when a service is migrated from CMEK to TDE.
	if config.EncryptionKey.IsNull() {
		plan.EncryptionKey = types.StringNull()
	}

	diags = resp.State.Set(ctx, plan)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}
}

// Delete deletes the resource and removes the Terraform state on success.
func (r *ServiceResource) Delete(ctx context.Context, req resource.DeleteRequest, resp *resource.DeleteResponse) {
	// Retrieve values from state
	var state models.ServiceResourceModel
	diags := req.State.Get(ctx, &state)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Delete existing order
	_, err := r.client.DeleteService(ctx, state.ID.ValueString())
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Deleting ClickHouse Service",
			"Could not delete service, unexpected error: "+err.Error(),
		)
		return
	}
}

